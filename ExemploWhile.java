//while é utilizado quando o nº de repetições não é conhecido previamente
//Enquanto a condição for verdadeira, irá imprimir os números
public class ExemploWhile {
    public static void main (String[] args){
        int i = 1;
        while (i <=5) {
            System.out.println("Número: "+ i);
            i++;
        }
    }
}